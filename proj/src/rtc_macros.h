#ifndef _RTC_MACROS_H_
#define _RTC_MACROS_H_

#define RTC_ADDR_REG			0x70 /**< @brief RTC's address register */

#define RTC_DATA_REG			0x71 /**< @brief RTC's data register */

#define RTC_MIN_ADDR			2 /**< @brief RTC's minute register address */
#define RTC_HOUR_ADDR			4 /**< @brief RTC's hour register address */
#define RTC_DAY_ADDR			7 /**< @brief RTC's day register address */
#define RTC_MONTH_ADDR			8 /**< @brief RTC's month register address */
#define RTC_YEAR_ADDR			9 /**< @brief RTC's year register address */

#define RTC_REG_A_ADDR			10 /**< @brief RTC's A register address */
#define RTC_REG_B_ADDR			11 /**< @brief RTC's B register address */
#define RTC_REG_C_ADDR			12 /**< @brief RTC's C register address */
#define RTC_REG_D_ADDR			13 /**< @brief RTC's D register address */

#endif
